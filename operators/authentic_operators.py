n1 = 10
n2 = 5

print(f'Considerando que o valor de n1 é {n1} e n2 é {n2}, então:')

# Operador de adição OBS: também é o operador de concatenação
print(f'A soma de {n1} + {n2} é {n1 + n2}')
print(f'A subtração de {n1} - {n2} é {n1 - n2}')
print(f'A multiplicação de {n1} x {n2} é {n1 * n2}')
print(f'A divisão de {n1} / {n2} é {n1 / n2}')
print(f'O resto da divisão de {n1} % {n2} é {n1 % n2}')
print(f'A exponenciação de {n1} ** {n2} é {n1 ** n2}')
print(f'O truncamento de {n1} // {n2} é {n1 // n2}')